package introduction_to_java.thuc_hanh;

public class CreateUse {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String str = "Ha Noi";
        System.out.println("i kiểu số nguyên, có giá trị là: "+i);
        System.out.println("f kiểu số thực, có giá trị là: "+f);
        System.out.println("d kiểu số thực lớn, có giá trị là: "+d);
        System.out.println("b kiểu logic, có giá trị là: "+b);
        System.out.println("c kiểu logic, có giá trị là: "+c);
        System.out.println("str kiểu logic, có giá trị là: "+str);
    }
}
